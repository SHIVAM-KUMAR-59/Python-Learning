# Some of the most user string functions in python:

# 1. len() function returns the length of a string
a = "hello"
print(len(a))

# 2. startsWith() function returns true if the string starts with the specified value, it is case sensitive
b = "hello"
print(b.startswith("h"))

# 3. endsWith() function returns true if the string ends with the specified value, it is case sensitive
c = "hello"
print(c.endswith("o"))

# 4. upper() function returns the string in uppercase
d = "hello"
print(d.upper())

# 5. lower() function returns the string in lowercase
e = "HELLO"
print(e.lower())

# 6. capitalize() function returns the string with first character in uppercase
f = "hello world"
print(f.capitalize())

# 7. count() function returns the number of times a substring occurs in a string
g = "hello"
print(g.count("l"))

# 8. replace() function returns a string where a specified value is replaced with a specified value, it creates a new string
h = "hello"
print(h.replace("hello", "world"))

# 9. split() function splits a string into a list
i = "hello"
print(i.split())

# 10. find() function returns the index of the first occurrence of a specified value
j = "hello"
print(j.find("l"))